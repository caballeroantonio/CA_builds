function isValidPhoneNumber(str){
    myregexp = new RegExp("^([_0-9() -;,]*)$");
    mymatch = myregexp.exec(str);
    if(str == "")
        return true;
    if(mymatch != null)
        return true;
    return false;
}
function isValidEmail(str){
    myregexp = new RegExp("^([a-zA-Z0-9_-]+\.)*[a-zA-Z0-9_-]+@[a-zA-Z0-9_-]+(\.[a-zA-Z0-9_-]+)*\.[a-zA-Z]{2,6}$");
    mymatch = myregexp.exec(str);
    if(str == "")
        return false;
    if(mymatch != null)
        return true;
    return false;
}        
